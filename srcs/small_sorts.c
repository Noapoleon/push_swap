#include "push_swap.h"

void	sort_three(t_stack *s)
{
	if (s->top->data < s->top->next->data
		&& s->top->next->data < s->top->prev->data)
		return ;
	if (s->top->data > s->top->next->data)
	{
		if (s->top->data < s->top->prev->data)
			return (swap(s));
		rot(s);
		if (s->top->data > s->top->next->data)
			swap(s);
		return ;
	}
	else if (s->top->data > s->top->prev->data)
		return (rrot(s));
	rrot(s);
	swap(s);
}

void	sort_four_five(t_push_swap *ps)
{
	while (ps->a->size > 3)
		push(ps->b, ps->a);
	sort_three(ps->a);
	while (ps->b->size)
		sort_closest(ps);
	rotate_to_min(ps);
}
